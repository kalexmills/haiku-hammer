#!/bin/bash
### BEGIN INIT INFO
# Provides:          haiku-hammer
# Required-Start:    $local_fs $network $named $time $syslog
# Required-Stop:     $local_fs $network $named $time $syslog
# Default-Start:     2 3 4 5
# Default-Stop:      0 1 6
# Description:       HaikuHammer is a discord bot
### END INIT INFO

SCRIPT=/home/botboy/haiku-hammer/server
RUNAS=botboy

PIDFILE=/var/run/haiku-hammer.pid
LOGFILE=/home/botboy/haiku-hammer/logs/haiku-hammer.log
ERRFILE=/home/botboy/haiku-hammer/logs/haiku-hammer.err

start() {
  if [ -f /var/run/$PIDNAME ] && kill -0 $(cat /var/run/$PIDNAME); then
    echo 'Service already running' >&2
    return 1
  fi
  echo 'Starting service' >&2
  su -s /bin/bash $RUNAS -c "cd haiku-hammer && $SCRIPT > $LOGFILE 2> $ERRFILE 0<&- &" > "$PIDFILE"
  echo 'Service started' >&2
}

stop() {
  if [ ! -f "$PIDFILE" ] || ! kill -0 $(cat "$PIDFILE"); then
    echo 'Service not running' >&2
    return 1
  fi
  echo 'Stopping serviceâ€¦' >&2
  kill -15 $(cat "$PIDFILE") && rm -f "$PIDFILE"
  echo 'Service stopped' >&2
}

uninstall() {
  echo -n "Are you really sure you want to uninstall this service? That cannot be undone. [yes|No] "
  local SURE
  read SURE
  if [ "$SURE" = "yes" ]; then
    stop
    rm -f "$PIDFILE"
    echo "Notice: log file is not be removed: '$LOGFILE'" >&2
    update-rc.d -f <NAME> remove
    rm -fv "$0"
  fi
}

case "$1" in
  start)
    start
    ;;
  stop)
    stop
    ;;
  uninstall)
    uninstall
    ;;
  restart)
    stop
    start
    ;;
  *)
    echo "Usage: $0 {start|stop|restart|uninstall}"
esac